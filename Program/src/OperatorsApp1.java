
public class OperatorsApp1 {

	public static int SubtractionNumbers(int a, int b) {
		return a-b;
	}
	public static int MultiplicationNumbers(int a, int b) {
		return a*b;
	}
	public static double DivisionNumbers(double a, double b) {
		return a/b;
	}
	public static int RemainderNumbers(int a, int b) {
		return a%b;
	}
}   
