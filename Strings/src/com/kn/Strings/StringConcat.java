package com.kn.Strings;

public class StringConcat {

	public static void main(String[] args) {
		String s1 = "Ram";
		System.out.println("s1 = " + s1);
		String s2 = "Sita";
		System.out.println("s2 = " + s2);

		// concatenation using concat
		s2 = s1.concat(s2);
		System.out.println("s1 = " + s1);
		System.out.println("s2 = " + s2);
		// System.out.println("s3 = " + s3);

	}

}
